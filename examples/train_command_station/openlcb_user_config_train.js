// Parameters factory.  Identifier shape mirrors the C-side pattern; the
// "user config" here is per-train rather than fixed-at-compile-time
// because the CS allocates trains dynamically on demand.
// ----------------------------------------------------------------------------

/**
 * Build createNode() parameters for one virtual train.
 *
 * @param {object} opts
 * @param {number} opts.addr   DCC address (1..10239).
 * @param {boolean} [opts.long]  Long DCC addressing.  Defaults to false.
 * @param {number}  [opts.steps] Speed-step preference (14, 28, or 128). Default 128.
 * @param {string}  [opts.name]  SNIP user name.  Default `DCC <addr>`.
 * @return {object} Parameters object suitable for openlcb.createNode(...).
 */
export function makeTrainNodeParameters({ addr, long = false, steps = 128, name } = {}) {

    return {

        // 1. snip
        snip: {
            mfgVersion:      4,
            name:            name || `DCC ${addr}${long ? ' (long)' : ''}`,
            model:           'OpenLcb Train 2000',
            hardwareVersion: '0.1',
            softwareVersion: '0.1',
            userVersion:     2,
        },

        // 2. protocol_support — PSI.TRAIN_CONTROL triggers automatic
        //    train_state allocation in the wrapper; the others enable the
        //    new CDI/FDI access via Memory Configuration.
        protocolSupport: [
            PSI.DATAGRAM,
            PSI.MEMORY_CONFIGURATION,
            PSI.EVENT_EXCHANGE,
            PSI.SIMPLE_NODE_INFORMATION,
            PSI.ABBREVIATED_DEFAULT_CDI,
            PSI.CONFIGURATION_DESCRIPTION_INFO,
            PSI.TRAIN_CONTROL,
        ],

        consumerCountAutocreate: 0,
        producerCountAutocreate: 0,

        configurationOptions: {
            readFromManufacturerSpace0xfcSupported: true,
            readFromUserSpace0xfbSupported:         true,
            writeToUserSpace0xfbSupported:          true,
            highestAddressSpace:                    0xFF,
            lowestAddressSpace:                     0xF9,
        },

        addressSpaceConfigurationDefinitionInfo: {
            present:         true,
            readOnly:        true,
            lowAddressValid: false,
            highestAddress:  _cdi_train_data.length - 1,
            description:     'Configuration Description Information',
        },

        addressSpaceAll: {
            present:         false,
            readOnly:        true,
            lowAddressValid: false,
            highestAddress:  0xFFFFFFFF,
            description:     'All Memory (debug)',
        },

        addressSpaceConfigMemory: {
            present:         true,
            readOnly:        false,
            lowAddressValid: false,
            highestAddress:  256,
            description:     'Configuration Memory',
        },

        addressSpaceAcdiManufacturer: {
            present:         true,
            readOnly:        true,
            lowAddressValid: false,
            highestAddress:  124,
            description:     'ACDI manufacturer access',
        },

        addressSpaceAcdiUser: {
            present:         true,
            readOnly:        false,
            lowAddressValid: false,
            highestAddress:  127,
            description:     'ACDI user access',
        },

        addressSpaceTrainFunctionDefinitionInfo: {
            present:         true,
            readOnly:        true,
            lowAddressValid: false,
            highestAddress:  _fdi_train_data.length - 1,
            description:     'Train FDI',
        },

        addressSpaceTrainFunctionConfigMemory: {
            present:         true,
            readOnly:        false,
            lowAddressValid: false,
            highestAddress:  0,  // library calculates from train function count at runtime
            description:     'Train function config memory',
        },

        addressSpaceFirmware: {
            present:         false,
            readOnly:        false,
            lowAddressValid: false,
            highestAddress:  0xFFFFFFFF,
            description:     'Firmware upgrade',
        },

        // 14-15. cdi / fdi byte arrays
        cdi: _cdi_train_data,
        fdi: _fdi_train_data,

        // Echo back addressing knobs so the CS app can call
        // setDccAddress / setSpeedSteps without re-deriving them.
        _train: { addr, long, steps },

    };

}
